class Pokemon {
  constructor(id, name, type, hp, attack, defense, speed, atk1, atk2, atk3, image) {
    this.id = id;
    this.name = name;
    this.type = type;
    this.hp = hp;
    this.attack = attack;
    this.defense = defense;
    this.speed = speed;
    this.atk1 = atk1;
    this.atk2 = atk2;
    this.atk3 = atk3;
    this.image = image;
  }
}